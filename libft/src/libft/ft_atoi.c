int	ft_atoi(const char *s)
{
	int	is_negative;
	int	num;

	is_negative = 0;
	num = 0;
	while (*s == '\t' || *s == '\n' || *s == '\v'
		|| *s == '\f' || *s == '\r' || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			is_negative++;
		s++;
	}
	while ('0' <= *s && *s <= '9')
	{
		num = (num * 10) + (*s - '0');
		s++;
	}
	if (is_negative)
		return (-num);
	return (num);
}
